/**
 * Shuffle array using Fisher-Yates algorithm
 */
function shuffleArray<T>(array: T[]): T[] {
  const shuffled = [...array];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled;
}

/**
 * Message rotation engine
 * - Shuffles template array once
 * - Iterates sequentially
 * - Reshuffles when exhausted
 * - Ensures no consecutive duplicates
 */
export class MessageRotationEngine {
  private templates: string[];
  private shuffledTemplates: string[];
  private currentIndex: number;
  private lastMessage: string;
  private reshuffled: boolean;

  constructor(categorySlug: string) {
    const categoryTemplates = commitTemplates[categorySlug] || commitTemplates["web_development"];
    this.templates = categoryTemplates;
    this.shuffledTemplates = shuffleArray(categoryTemplates);
    this.currentIndex = 0;
    this.lastMessage = "";
    this.reshuffled = false;
  }

  /**
   * Get next message ensuring no consecutive duplicates
   */
  getNextMessage(): string {
    // If we've gone through all messages, reshuffle
    if (this.currentIndex >= this.shuffledTemplates.length) {
      this.shuffledTemplates = shuffleArray(this.templates);
      this.currentIndex = 0;
      this.reshuffled = true;
    }

    // Get current message
    let message = this.shuffledTemplates[this.currentIndex];
    
    // If this is the same as the last message, skip to next
    while (message === this.lastMessage && this.shuffledTemplates.length > 1) {
      this.currentIndex++;
      
      // If we've gone through all messages, reshuffle
      if (this.currentIndex >= this.shuffledTemplates.length) {
        this.shuffledTemplates = shuffleArray(this.templates);
        this.currentIndex = 0;
        this.reshuffled = true;
      }
      
      message = this.shuffledTemplates[this.currentIndex];
    }

    // Update tracking
    this.lastMessage = message;
    this.currentIndex++;

    return message;
  }

  /**
   * Generate multiple messages
   */
  getMessages(count: number): string[] {
    const messages: string[] = [];
    for (let i = 0; i < count; i++) {
      messages.push(this.getNextMessage());
    }
    return messages;
  }

  /**
   * Reset the engine
   */
  reset(): void {
    this.shuffledTemplates = shuffleArray(this.templates);
    this.currentIndex = 0;
    this.lastMessage = "";
    this.reshuffled = false;
  }

  /**
   * Check if messages were reshuffled
   */
  wasReshuffled(): boolean {
    return this.reshuffled;
  }
}

/**
 * Get commit messages for a category
 */
export function getCommitMessages(category: string, count: number): string[] {
  const slug = CATEGORY_SLUGS[category] || "web_development";
  const engine = new MessageRotationEngine(slug);
  return engine.getMessages(count);
}

/**
 * Generate file content with commit messages
 */
export function generateFileContent(commitMessages: string[]): string {
  const lines: string[] = [
    "# Educational Commit Log",
    "# This file tracks the commit history for learning purposes",
    "",
    "## Commits",
    "",
  ];
  
  commitMessages.forEach((message, index) => {
    const date = new Date().toISOString();
    lines.push(`${index + 1}. ${message}`);
    lines.push(`   Date: ${date}`);
    lines.push("");
  });
  
  return lines.join("\n");
}

